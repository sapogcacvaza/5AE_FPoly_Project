    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        txtMa.setText("");
        if (checkValidate()) {
            try {
                ServiceCategory sc = getForm();
                scr.add(sc);

                javax.swing.JOptionPane.showMessageDialog(this, "Thêm thành công!");
                showTable(scr.getAll());
                clearForm();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Lỗi thêm!");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (txtMa.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn loại để sửa!");
            return;
        }

        if (checkValidate()) {
            try {
                ServiceCategory sc = getForm();
                scr.update(sc);

                javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
                showTable(scr.getAll());
                clearForm();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Lỗi cập nhật!");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (txtMa.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn loại để xoá!");
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Bạn có chắc muốn xoá không?",
                "Xác nhận",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                int id = Integer.parseInt(txtMa.getText().trim());
                scr.delete(id);

                javax.swing.JOptionPane.showMessageDialog(this, "Xoá thành công!");
                showTable(scr.getAll());
                clearForm();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Không thể xoá! Có thể đang được sử dụng.");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

