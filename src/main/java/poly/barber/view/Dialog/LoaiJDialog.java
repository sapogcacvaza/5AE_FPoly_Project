    private void showTable(List<ServiceCategory> all) {
        dtm.setRowCount(0);
        this.listGlobal = all;

        List<ServiceCategory> listCategories = scr.getAll();

        for (ServiceCategory s : listCategories) {
            dtm.addRow(new Object[]{
                s.getServiceCategoryID(),
                s.getServiceCategoryName()
            });
        }
    }

    private void detail(List<ServiceCategory> all, int index) {
        ServiceCategory s = all.get(index);
        txtMa.setText(s.getServiceCategoryID() + "");
        txtTen.setText(s.getServiceCategoryName());
    }

    private ServiceCategory getForm() {
        ServiceCategory s = new ServiceCategory();
        String ma = txtMa.getText().trim();
        if (!ma.isEmpty()) {
            s.setServiceCategoryID(Integer.parseInt(ma));
        }
        s.setServiceCategoryName(txtTen.getText().trim());
        return s;
    }

    private boolean checkValidate() {
        String ten = txtTen.getText().trim();
        String maStr = txtMa.getText().trim();

        if (ten.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Tên dịch vụ không được để trống!");
            txtTen.requestFocus();
            return false;
        }

        ServiceCategory sCheck = scr.getByName(ten);

        if (sCheck != null) {
            if (maStr.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Tên dịch vụ đã tồn tại!");
                return false;
            } else {
                int idForm = Integer.parseInt(maStr);

                if (sCheck.getServiceCategoryID() != idForm) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Tên dịch vụ đã tồn tại ở bản ghi khác!");
                    return false;
                }
            }
        }
        return true;
    }

    private void clearForm() {
        txtMa.setText("");
        txtTen.setText("");
    }
